package com.example.smartlife_api;

public class Login_Api {

	private String status;
	private String id;
	private String name;
	private String fid;
	private String fid_t;
	private String vs;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getFid() {
		return fid;
	}

	public void setFid(String fid) {
		this.fid = fid;
	}

	public String getFid_t() {
		return fid_t;
	}

	public void setFid_t(String fid_t) {
		this.fid_t = fid_t;
	}

	public String getVs() {
		return vs;
	}

	public void setVs(String vs) {
		this.vs = vs;
	}

}
